    write32(dsbhead + 0xcc, a9);
    write32(dsbhead + 0xd0, l9);
    write32(dsbhead + 0xd4, l9 + 0x138 - 0x0c);
    write32(dsbhead + 0xd8, a7);
    write32(dsbhead + 0xdc, l7);
    for (; i < 0x200; i++)
        dsbhead[i] = dsbhead[i] ^ key;
    fwrite(dsbhead, 1, 0x200, f);
    fwrite(p9, 1, l9, f);
    fwrite(p7, 1, l7, f);
    fclose(f);
